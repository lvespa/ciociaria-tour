def build_pages():
    for tour in tours_data:
        # Generate program list items
        prog_it_html = "\\n        ".join([f"<li>{item}</li>" for item in tour["prog_it"]])
        prog_en_html = "\\n        ".join([f"<li>{item}</li>" for item in tour["prog_en"]])
        
        # Generate details items
        details_html = ""
        keys_it = list(tour["details_it"].keys())
        keys_en = list(tour["details_en"].keys())
        
        for k_it, k_en in zip(keys_it, keys_en):
            val_it = tour["details_it"][k_it]
            val_en = tour["details_en"][k_en]
            details_html += f'''
      <div class="detail-item">
        <div class="detail-label">
           <span data-lang="it">{k_it}</span>
           <span data-lang="en">{k_en}</span>
        </div>
        <div class="detail-value">
           <span data-lang="it">{val_it}</span>
           <span data-lang="en">{val_en}</span>
        </div>
      </div>'''
        
        page_html = HTML_TEMPLATE.format(
            title_it=tour["title_it"],
            title_en=tour["title_en"],
            image=tour["image"],
            category_it=tour["category_it"],
            category_en=tour["category_en"],
            desc_it=tour["desc_it"],
            desc_en=tour["desc_en"],
            program_html_it=prog_it_html,
            program_html_en=prog_en_html,
            details_html=details_html
        )
        
        with open(f"tour-{tour['id']}.html", "w", encoding="utf-8") as f:
            f.write(page_html)
    
    print(f"Generated {len(tours_data)} pages.")

if __name__ == '__main__':
    build_pages()
